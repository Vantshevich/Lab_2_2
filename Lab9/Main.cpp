int main() {
	ofstream Output("Output.txt");
	setlocale(LC_ALL, ".1251");

	CQueue<int> arrCars;
	int n, m, x, k;
	cout << "Введите начальное кол-во машин: ";
	cin >> n;
	cout << "Ввидет кол-во приезжающих машин: ";
	cin >> k;
	cout << "Введите кол-во машин проезжающее за один промежуток: ";
	cin >> m;
	if (m > n) m = n;

	ifstream Input("Data.txt");
	if (Input.is_open())
	{
		int in;
		while (Input >> in)			
		{
			arrCars.Push(in);
		}
	}
	Input.close();


	cout << "Введите номер проверяемой машины: " << endl;
	cin >> x;

	for (int i = 0; i < m; i++)
	{
		if (arrCars.Front() == x) {
			x = 0;
			if (Output.is_open())
			{
				Output << "Yes";
				cout << "Yes";
			}
			break;
		}
		arrCars.Pop();
	}
	if (x)
	{
		if (Output.is_open())
		{
			Output << "No";
			cout << "No";
		}
	}

	Output.close();

	system("pause");
	return 0;
}